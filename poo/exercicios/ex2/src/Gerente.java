public class Gerente {
    private String nome;
    private String email;
    private int ramal;

    public String getNome() {
        return nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getRamal() {
        return ramal;
    }

    public void setRamal(int ramal) {
        this.ramal = ramal;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
